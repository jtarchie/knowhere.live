import { route } from "preact-router";
import qs from "qs";
import { Manager } from "../render/manager";

function BottomNav({ manifestName }: { manifestName?: string }) {
  const params = qs.parse(window.location.search);

  const currentPath = window.location.pathname;
  const active = (path: string) => currentPath == path ? "active" : "";
  const redirect = (path: string) => () => route(path);

  return (
    <div class="btm-nav bg-base-100 flex">
      <button
        class={active(`/beta/${manifestName}/map`)}
        onClick={redirect(`/beta/${manifestName}/map`)}
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          stroke-width="1.5"
          stroke="currentColor"
          class="size-6"
        >
          <path
            stroke-linecap="round"
            stroke-linejoin="round"
            d="M9 6.75V15m6-6v8.25m.503 3.498 4.875-2.437c.381-.19.622-.58.622-1.006V4.82c0-.836-.88-1.38-1.628-1.006l-3.869 1.934c-.317.159-.69.159-1.006 0L9.503 3.252a1.125 1.125 0 0 0-1.006 0L3.622 5.689C3.24 5.88 3 6.27 3 6.695V19.18c0 .836.88 1.38 1.628 1.006l3.869-1.934c.317-.159.69-.159 1.006 0l4.994 2.497c.317.158.69.158 1.006 0Z"
          />
        </svg>
        <span class="btm-nav-label">Map</span>
      </button>
      {params.editor && (
        <button
          class={active("/beta/editor")}
          onClick={redirect("/beta/editor")}
        >
          <svg
            xmlns="http://www.w3.org/2000/svg"
            fill="none"
            viewBox="0 0 24 24"
            stroke-width="1.5"
            stroke="currentColor"
            class="size-6"
          >
            <path
              stroke-linecap="round"
              stroke-linejoin="round"
              d="m16.862 4.487 1.687-1.688a1.875 1.875 0 1 1 2.652 2.652L10.582 16.07a4.5 4.5 0 0 1-1.897 1.13L6 18l.8-2.685a4.5 4.5 0 0 1 1.13-1.897l8.932-8.931Zm0 0L19.5 7.125M18 14v4.75A2.25 2.25 0 0 1 15.75 21H5.25A2.25 2.25 0 0 1 3 18.75V8.25A2.25 2.25 0 0 1 5.25 6H10"
            />
          </svg>
          <span class="btm-nav-label">Editor</span>
        </button>
      )}
      <button
        class={active(`/beta/${manifestName}/filter`)}
        onClick={redirect(`/beta/${manifestName}/filter`)}
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          stroke-width="1.5"
          stroke="currentColor"
          class="size-6"
        >
          <path
            stroke-linecap="round"
            stroke-linejoin="round"
            d="M10.5 6h9.75M10.5 6a1.5 1.5 0 1 1-3 0m3 0a1.5 1.5 0 1 0-3 0M3.75 6H7.5m3 12h9.75m-9.75 0a1.5 1.5 0 0 1-3 0m3 0a1.5 1.5 0 0 0-3 0m-3.75 0H7.5m9-6h3.75m-3.75 0a1.5 1.5 0 0 1-3 0m3 0a1.5 1.5 0 0 0-3 0m-9.75 0h9.75"
          />
        </svg>

        <span class="btm-nav-label">Filter</span>
      </button>
      <button
        onClick={async () => {
          const manager = new Manager();
          const manifest = manager.load(manifestName);
          console.log(manifest, { values: manifest.filterValues });
          const url =
            `${import.meta.env.VITE_WEB_ADDRESS}/beta/${manifestName}/map?${
              qs.stringify({ values: manifest.filterValues })
            }`;

          try {
            await navigator.share({
              title: "Knowhere to Live",
              url: url,
            });
          } catch (err) {
            console.log("Native share not supported", err);
            navigator.clipboard.writeText(url);
          }
        }}
      >
        <svg
          xmlns="http://www.w3.org/2000/svg"
          fill="none"
          viewBox="0 0 24 24"
          stroke-width="1.5"
          stroke="currentColor"
          class="size-6"
        >
          <path
            stroke-linecap="round"
            stroke-linejoin="round"
            d="M7.217 10.907a2.25 2.25 0 1 0 0 2.186m0-2.186c.18.324.283.696.283 1.093s-.103.77-.283 1.093m0-2.186 9.566-5.314m-9.566 7.5 9.566 5.314m0 0a2.25 2.25 0 1 0 3.935 2.186 2.25 2.25 0 0 0-3.935-2.186Zm0-12.814a2.25 2.25 0 1 0 3.933-2.185 2.25 2.25 0 0 0-3.933 2.185Z"
          />
        </svg>

        <span class="btm-nav-label">Share</span>
      </button>
    </div>
  );
}

export { BottomNav };
